//Crea una función declarada llamada calcularAreaTriangulo que reciba dos
// parámetros: la base y la altura de un triángulo. La función debe devolver el
// área del triángulo.

// crear la función 
const calcularAreaTriangulo = (base, altura) => base * altura / 2;

// llamar a la función
console.log(calcularAreaTriangulo(5, 10)); // 25



